package com.example.springlombak.Model;

public class BeerStyle {
    private String beerStyle;
    private String beerStyleDescription;

    public String getBeerStyle() {
        return beerStyle;
    }

    public void setBeerStyle(String beerStyle) {
        this.beerStyle = beerStyle;
    }

    public String getBeerStyleDescription() {
        return beerStyleDescription;
    }

    public void setBeerStyleDescription(String beerStyleDescription) {
        this.beerStyleDescription = beerStyleDescription;
    }
}
